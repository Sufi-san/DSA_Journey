package _3_Assignments._3_ConditionalsAndLoops;

public class GoOutDays {
    // August has 31 days
    // Kunal is allowed to go out on the even days of the month only
    // To find number of even days in a month, we can just divide the number of days by '2'.
    public static void main(String[] args) {
        System.out.println("Number of Days in August: "+31);
        System.out.println("Number of Even Days in August: "+ 31/2);
        System.out.println("Number of Days Kunal can go out: "+31/2);
    }
}
